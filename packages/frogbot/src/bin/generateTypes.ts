// The `frogbot generate:types` command.
//
// Full pipeline end-to-end: load config → JSON schema → compile TypeScript
// → diff → write to disk.
//
// We deliberately do not call Payload's `generateTypes` directly:
// Payload's version always writes to disk (no `returnString` in 3.68.5),
// emits a Payload-branded banner, and appends a `'payload'`
// augmentation footer that would need post-processing. Calling the
// public `configToJSONSchema` and compiling ourselves avoids all of
// that — no temp file, no string rewrite, FrogBot branding throughout.
//
// Output is deterministic — running twice in a row leaves the file
// unchanged (byte-identical short-circuit before write).

import { isAbsolute, join, resolve } from 'node:path';
import fs from 'node:fs/promises';

import { initI18n } from '@payloadcms/translations';
import type { AcceptedLanguages } from '@payloadcms/translations';
import { compile } from 'json-schema-to-typescript';
import { configToJSONSchema } from 'payload';

import type { SanitizedConfig } from 'payload';
import { loadConfig } from '../config/load.js';

const BANNER = `/* tslint:disable */
/* eslint-disable */
/**
 * This file was automatically generated by FrogBot.
 * DO NOT MODIFY IT BY HAND. Instead, modify your source FrogBot config,
 * and re-run \`frogbot generate:types\` to regenerate this file.
 */`;

export function buildGeneratedTypesFooter(agentSlugs: readonly string[]): string {
  const agents =
    agentSlugs.length === 0
      ? '{}'
      : `{
${agentSlugs.map((slug) => `      ${JSON.stringify(slug)}: unknown;`).join('\n')}
    }`;

  return `declare module 'frogbot' {
  export interface GeneratedTypes extends Config {
    agents: ${agents};
  }
}`;
}

/** Default output filename, matching Payload's `payload-types.ts` convention. */
const DEFAULT_FILENAME = 'frogbot-types.ts';

function resolveOutputPath(config: SanitizedConfig, cwd: string): string {
  const fromEnv = process.env.FROGBOT_TS_OUTPUT_PATH;
  if (fromEnv) return isAbsolute(fromEnv) ? fromEnv : resolve(cwd, fromEnv);

  const fromConfig = config.typescript?.outputFile;
  // Payload defaults `typescript.outputFile` to `<cwd>/payload-types.ts`
  // during sanitization. If the user hasn't customized it, redirect to
  // FrogBot's filename so we don't litter `payload-types.ts` next to
  // the user's config. Otherwise honor whatever they explicitly set.
  if (fromConfig && !fromConfig.endsWith('/payload-types.ts')) return fromConfig;
  return join(cwd, DEFAULT_FILENAME);
}

async function compileTypes(config: SanitizedConfig, agentSlugs: readonly string[]): Promise<string> {
  const languages = Object.keys(config.i18n.supportedLanguages) as AcceptedLanguages[];
  const language = languages.includes('en') ? 'en' : config.i18n.fallbackLanguage;
  const i18n = await initI18n({
    config: config.i18n,
    context: 'api',
    language,
  });

  // Payload's helper returns `{ jsonSchema, typeStringDefinitions }` in
  // newer versions; 3.68.5 returns the bare schema. Normalize.
  const result = configToJSONSchema(config, config.db.defaultIDType, i18n) as
    | { jsonSchema: unknown; typeStringDefinitions?: Set<string> }
    | object;
  const jsonSchema = 'jsonSchema' in result ? (result as { jsonSchema: unknown }).jsonSchema : result;
  const extraTypeStrings =
    'typeStringDefinitions' in result
      ? (result as { typeStringDefinitions?: Set<string> }).typeStringDefinitions
      : undefined;

  let compiled = await compile(jsonSchema as Parameters<typeof compile>[0], 'Config', {
    bannerComment: BANNER,
    style: { singleQuote: true },
    // Emit code for $defs not referenced elsewhere — preserves user
    // `interfaceName` opt-ins even when nothing references them.
    unreachableDefinitions: true,
    cwd: process.cwd(),
  });

  if (extraTypeStrings && extraTypeStrings.size > 0) {
    compiled = `${compiled.trimEnd()}\n\n${[...extraTypeStrings].join('\n\n')}\n`;
  }

  return `${compiled.trimEnd()}\n\n\n${buildGeneratedTypesFooter(agentSlugs)}\n`;
}

export async function generateTypes(): Promise<void> {
  const cwd = process.cwd();

  try {
    const frogbotConfig = await loadConfig(cwd);
    const config = await frogbotConfig._internal.payloadConfig;
    const outputPath = resolveOutputPath(config, cwd);
    const compiled = await compileTypes(config, frogbotConfig.agents?.map((agent) => agent.slug) ?? []);

    try {
      const existing = await fs.readFile(outputPath, 'utf-8');
      if (existing === compiled) {
        console.log(`[frogbot] types unchanged at ${outputPath}`); // eslint-disable-line no-console
        return;
      }
    } catch {
      // No existing file — fall through to write.
    }

    await fs.writeFile(outputPath, compiled);
    console.log(`[frogbot] types written to ${outputPath}`); // eslint-disable-line no-console
  } catch (err) {
    const message = err instanceof Error ? err.message : String(err);
    console.error(`[frogbot] ${message}`); // eslint-disable-line no-console
    process.exit(1);
  }
}
